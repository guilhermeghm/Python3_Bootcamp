#For version
print("Using for: " )
for n in range(1,11):
    print("\U0001f600"*n)

#Another option using for
print (" ")
print("Another option using for for to print multiple time: " )
for x in range (3):
    for n in range(1,11):
        print("\U0001f600"*n)

#While version
print (" ")
print("Using while: ")
n = 1
while n < 11:
    print("\U0001f600"*n)
    n += 1
